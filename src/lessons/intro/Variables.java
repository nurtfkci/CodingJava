package lessons.intro;

public class Variables {
	
	public static void main(String[] args) {
		System.out.print("Hello"); // prints 
		System.out.println("World!"); // prints + new line
		
		// CONCATENATION
		System.out.print("I am learning Java!" + "\t\nThis is my first program!");
		
		// n : new line     t : tab space
		
		System.out.println("\nI am " + 23 + " years old!");
		System.out.println("My favorite number is " + (1902+1) + "!");
		
		// Variable : 
		// Name, Type, Value, Address
		// dataType variablenName = value;
		
		// *** PRIMITIVE DATA TYPES ***
		// Numbers
		// Whole Numbers - Byte - Short - Integer - Long (l or L)
		// Fractional/Decimal Numbers - Float (f or F) - Double
		
		// Characters - Char - single quotes
		// Boolean/Logical - Boolean
		
		// *** REFERENCE - NON PRIMITIVE ***
		// String : sequence of characters
		
	}

}
