package lessons.special;

public class MathClass {

	// ceil : round up to bigger numm
	// cos : returns cos value
	// sin : returns sin value
	// floor : round up to lower num
	// max(x,y) : returns bigger num
	// min(x,y) : returns lower num
	// pow(3,5) : 3^5
	// random() : random num between 1 and 0
	// round() : round up to closes num
	// abs : absolute value
	// sqrt() : square root

	public static void main(String[] args) {

		System.out.println(Math.round(4.4));
		System.out.println(Math.ceil(4.3));
		System.out.println(Math.floor(3.4));

	}

}
